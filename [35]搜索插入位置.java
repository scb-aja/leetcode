class Solution {
    public int searchInsert(int[] nums, int target) {
         int left = 0;
         int right = nums.length - 1;

         if(target >= nums[nums.length - 1])
             return nums.length;
         while (left < right){
             int middle = left + (right - left)/2;
             if(nums[middle] > target)
                 left = middle + 1;
             else{
                 right = middle;
             }
         }
         return left;






    }
}
