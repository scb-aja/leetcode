// @lc code=start
/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public int maxDepth(TreeNode root) {
        //base case
        if(root == null){
            return 0;
        }
        int leftHeight = maxDepth(root.left); //得到左子树的深度
        int rightHeight = maxDepth(root.right); //得到右子树的深度
        return Math.max(leftHeight , rightHeight) + 1; //返回当前节点的深度
    }
}
